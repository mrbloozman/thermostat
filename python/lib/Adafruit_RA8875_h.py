import enum

class RA8875sizes(enum.Enum):
	RA8875_480x272 = 1
	RA8875_800x480 = 2

class tsPoint_t:
	def __init__(self,x,y):
		self.x = int(x)
		self.y = int(y)

class tsMatrix_t:
	def __init__(self,An,Bn,Cn,Dn,En,Fn,Divider):
		self.An = int(An)
		self.Bn = int(Bn)
		self.Cn = int(Cn)
		self.Dn = int(Dn)
		self.En = int(En)
		self.Fn = int(Fn)
		self.Divider = int(Divider)

