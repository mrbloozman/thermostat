def bitSwap(b):
	x = 0b00000000
	for i in range(8):
		if (b&(1<<i)):
			x+=(128>>i)
	return x

def bitSwapList(l):
	i=0
	for f in l:
		newpic=[]
		for b in f:
			newpic.append(bitSwap(b))
		l[i]=newpic
		i+=1
	return l